public class QuttationList {
	private String plasticItem;
	private String status;
	private String approveBy;
	private String editBy;
	private String datetime;
	private String suggestPrice;
	public String getPlasticItem() {
		return plasticItem;
	}
	public void setPlasticItem(String plasticItem) {
		this.plasticItem = plasticItem;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getApproveBy() {
		return approveBy;
	}
	public void setApproveBy(String approveBy) {
		this.approveBy = approveBy;
	}
	public String getEditBy() {
		return editBy;
	}
	public void setEditBy(String editBy) {
		this.editBy = editBy;
	}
	public String getDatetime() {
		return datetime;
	}
	public void setDatetime(String datetime) {
		this.datetime = datetime;
	}
	public String getSuggestPrice() {
		return suggestPrice;
	}
	public void setSuggestPrice(String suggestPrice) {
		this.suggestPrice = suggestPrice;
	}
	
	
}
